function estacoes(mes){
    if( mes < 3 || mes == 12){
        console.log(`O mês ${mes} é Verão`)
    }
    else if( mes < 6){
        console.log(`O mês ${mes} é Outono`)
    }
    else if( mes < 9){
        console.log(`O mês ${mes} é Inverno`)
    }
    else{
        console.log(`O mês ${mes} é Primavera`)
    }
}
estacoes (2)
